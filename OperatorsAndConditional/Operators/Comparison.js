//Subtraction Assignment
a -= 3; // Equivalent to a = a - 3, now a is 12
console.log(a); // Output: 12

//Multiplication Assignment
a *= 2; // Equivalent to a = a * 2, now a is 24
console.log(a); // Output: 24
//Division Assignment
a /= 4;
// Equivalent to a = a / 4, now a is 6
console.log(a); // Output: 6

//Modulus Assignment
a %= 5; // Equivalent to a = a % 5, now a is 1
console.log(a); // Output: 1

//Exponentiation Assignment
a **= 3; // Equivalent to a = a ** 3, now a is 1 (1 raised to the power of 3 is 1)
console.log(a); // Output: 1

//Comparison Operators
console.log(10 == "10"); // Equality (loose) - true, because it converts the string to a number
console.log(10 === "10"); // Strict Equality - false, because they are of different types
console.log(10 != "10"); // Inequality (loose) - false, because it converts the string to a number
console.log(10 !== "10"); // Strict Inequality - true, because they are of different types
console.log(10 > 5); // Greater than - true
console.log(10 < 5); // Less than - false
console.log(10 >= 10); // Greater than or equal to - true
console.log(10 <= 9); // Less than or equal to - false


