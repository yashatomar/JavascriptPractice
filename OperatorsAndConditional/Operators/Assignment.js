// Assignment Operators
console.log(10+3); // Addition
console.log(10-3); // Subtraction
console.log(10*3); // Multiplication
console.log(10/3); // Division
console.log(10%3); // Modulus (Remainder)
console.log(10**3); // Exponentiation (10 raised to the power of 3)

//Increment and Decrement
console.log(10++); // Post-increment (returns 10, then increments to 11)
console.log(++10); // Pre-increment (increments to 11, then returns 11)
console.log(10--); // Post-decrement (returns 10, then decrements to 9)
console.log(--10); // Pre-decrement (decrements to 9, then returns 9)

//Compound Assignment
let x = 10;
x += 5; // Equivalent to x = x + 5
console.log(x); // 15

x -= 3; // Equivalent to x = x - 3
console.log(x); // 12
